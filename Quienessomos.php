            <td rowspan="3" align="left" valign="top" class="box1"><p class="Tit_Pagina">Misi&oacute;n</p>
              <p>Formar alumnos pensantes, cr&iacute;ticos y creativos, con habilidad para expresarse en forma oral y escrita, con pensamiento l&oacute;gico y mentalidad cient&iacute;fica, un alumno identificado con su nacionalidad, sensible a las manifestaciones culturales; que alcance el dominio del italiano e ingl&eacute;s como idiomas extranjeros y que logre un desarrollo f&iacute;sico a trav&eacute;s del deporte. Buen amigo, leal, honesto, seguro de si mismo, amante de las artes, capaz de trabajar y divertirse en un ambiente de franca cordialidad y camarader&iacute;a.</p>
              <p class="Tit_Pagina">Visi&oacute;n</p>
              <p>Un ambiente de estudio planificado, organizado, educando en positivo, por medio de un sistema de trabajo y est&iacute;mulo continuo, mejorando cada d&iacute;a los procesos, revis&aacute;ndolos continuamente y aplicando los correctivos necesarios en forma inmediata. Teniendo siempre presente que el mejor aprendizaje y el m&aacute;s creativo es el que se inscribe en los valores y por ende en la excelencia.</p>
              <p class="Tit_Pagina">Estructura acad&eacute;mica</p>
              <p class="Tit_Blanco">Pre-escolar</p>
              <p>Nuestro Prescolar ofrece a los ni&ntilde;os de 3 a 6 a&ntilde;os la oportunidad de: <br>
            -Aprender activamente a trav&eacute;s de experiencias directas con el medio ambiente y los recursos que los rodean.  <br>
            -Participar en actividades que favorecen su desarrollo integral:  <br>
            -Actividades musicales y deportivas.  <br>
            -Talleres de creatividad y expresi&oacute;n corporal.  <br>
            -Cer&aacute;mica, manualidades, cocina y juegos educativos.  <br>
            -Actividades que facilitan el aprendizaje del c&iacute;rculo y de la lecto-escritura Adem&aacute;s podr&aacute;n participar en actividades que contribuyen con el aprendizaje de la cultura y la lengua italiana, mediante material  did&aacute;ctico reconocido por el Gobierno Italiano, facilitando desdeesta edad la posibilidad de una formaci&oacute;n biling&uuml;e en el alumno. </p>
            <p class="Tit_Blanco">Escuela B&aacute;sica </p>
            <p>La Escuela B&aacute;sica cumple con las nuevas disposiciones del Ministerio de Educaci&oacute;n de manera de lograr un  desarrollo integral de nuetros alumnos. Paralelamente nuestra escuela ofrece, en forma regular programas reconocidos de la lengua y cultura italiana, desde Pre-escolar hasta 9no. a&ntilde;o de b&aacute;sica que capacitan a nuestros educandos para continuar, si as&iacute; lo desean estudios en la Escuela Italiana.   </p>
            <p class="Tit_Blanco">Ciclo Diversificado </p>
            <p>El Ciclo Diversificado cumple con lossiguientes objetivos en sus educando: <br>
            - Desarrollo de pensamiento reflexivo y cr&Igrave;tico.<br>  
            - Aplicaci&oacute;n de los mejores recursos pedag&oacute;gicos para un &oacute;ptimo aprendizaje.  <br>
            - Incorporaci&oacute;n de la inform&aacute;tica en las situaciones de aprendizaje.  <br>
            - Formaci&oacute;n integral del educando como individuo &uacute;til a la comunidad.  <br>
            - Actividades deportivas que complementan el desarrollo integral del estudiante</p></td>
